	void Draw::DrawModel(const MeshRendererComponent& mrc,glm::vec3 position, glm::vec3 scale,glm::vec3 rotation) {
		glm::mat4 m_model = mrc.m_model;
		m_model = glm::translate(m_model, position);
		//if(rotation != 0.0f) m_model = glm::rotate(glm::mat4(1.0f), glm::radians(rotation), { 0.0f, 0.0f, 1.0f });
		m_model = glm::scale(m_model, scale);
		mrc.shader->setMat4("model", m_model);
		mrc.shader->setVec4("color", mrc.color);
		if(mrc.model) {
			mrc.model->Draw(*mrc.shader);
		}
		mrc.shader->setVec4("color", BLANK);
	}
	void Draw::DrawPrimitive(PrimitiveRendererComponent& prc, glm::vec3 position, glm::vec3 scale, glm::vec3 rotation) {
		switch(prc.primitive) {
		case CUBE:
			DrawCube(*prc.shader, prc.color, position, scale, rotation);
			break;
		case QUAD:
			//DrawQuad();
			break;
		case NONE:

			break;
		default:
			DrawCube(*prc.shader, prc.color, position, scale, rotation);
			break;
		}
	}
	glm::mat4 Draw::DrawCube(Shader& shader,glm::vec4 color,glm::vec3 position, glm::vec3 scale, glm::vec3 rotation) {
		glm::mat4 m_model = glm::mat4(1.0f);
		m_model = glm::translate(m_model, position);
		if(rotation != glm::vec3(0)) m_model = glm::rotate(m_model, glm::radians(0.00f), rotation);
		m_model = glm::scale(m_model, scale);
		shader.setVec4("color", color);
		shader.setMat4("model", m_model);
		render_cube();
		shader.setVec4("color", BLANK);
		return m_model;
	}

